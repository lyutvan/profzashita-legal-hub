const getShortTitle = (title: string) => {
  const normalized = title.replace(/[«»"]/g, "").trim();
  const cutBy = ["(", "—", " / ", " | "];
  for (const sep of cutBy) {
    const idx = normalized.indexOf(sep);
    if (idx > 0) return normalized.slice(0, idx).trim();
  }
  return normalized;
};

